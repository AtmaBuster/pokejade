; Characteristics of each move.

MACRO move
	; the animation byte will be filled when the move is loaded
	db \1 ; effect
	db \2 ; power
	db \3 | \4 ; type & category
	db \5 percent ; accuracy
	db \6 ; pp
	db \7 percent ; effect chance
	db \8 ; flags
	assert \6 <= 40, "PP must be 40 or less"
ENDM

